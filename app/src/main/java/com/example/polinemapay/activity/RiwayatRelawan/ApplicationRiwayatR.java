package com.example.polinemapay.activity.RiwayatRelawan;

public class ApplicationRiwayatR {

    private String idAcara;
    private String namaAcara;
    private String tanggalJemput;
    private String waktuJemput;
    private String perkiraanBeratSampah;
    private String alamatJemput;

    public String getIdAcara() {
        return idAcara;
    }

    public void setIdAcara(String idAcara) {
        this.idAcara = idAcara;
    }

    public String getNamaAcara() {
        return namaAcara;
    }

    public void setNamaAcara(String namaAcara) {
        this.namaAcara = namaAcara;
    }

    public String getTanggalJemput() {
        return tanggalJemput;
    }

    public void setTanggalJemput(String tanggalJemput) {
        this.tanggalJemput = tanggalJemput;
    }

    public String getWaktuJemput() {
        return waktuJemput;
    }

    public void setWaktuJemput(String waktuJemput) {
        this.waktuJemput = waktuJemput;
    }

    public String getPerkiraanBeratSampah() {
        return perkiraanBeratSampah;
    }

    public void setPerkiraanBeratSampah(String perkiraanBeratSampah) {
        this.perkiraanBeratSampah = perkiraanBeratSampah;
    }

    public String getAlamatJemput() {
        return alamatJemput;
    }

    public void setAlamatJemput(String alamatJemput) {
        this.alamatJemput = alamatJemput;
    }
}
